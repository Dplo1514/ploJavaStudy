public class part_9_ifelse {
    public static void main(String[] args) {

        int apple = 200;

        if (apple > 100) { //true일 때의 실행값
            System.out.println("I don't like apple");
        }
        else { //false일 때의 실행값
            System.out.println("I like apple");
        }

    }
}
